    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        boolean datosCorrectos = true;
        String nombre = txtNombre.getText().trim();
        LocalDate fecha = datepicker.getDate();
        int mesa = -1;
        try {
            mesa = Integer.parseInt(txtMesa.getText().trim());
            if (mesa <= 0) {
                throw new Exception();
            }
        } catch (Exception ex) {
            datosCorrectos = false;
            JOptionPane.showMessageDialog(this, "La mesa tiene que ser un número entero positivo.");
        }
        float totalMaximo = -1;
        try {
            totalMaximo = Float.parseFloat(txtTotalMaximo.getText().trim());
            if (totalMaximo < 0) {
                throw new Exception();
            }
        } catch (Exception ex) {
            datosCorrectos = false;
            JOptionPane.showMessageDialog(this, "El total máximo tiene que ser un número.");
        }

        if (datosCorrectos) {
            if (Float.compare(totalMaximo, total) >= 0) {
                if (Float.compare(total, 0) > 0) {
                    Comanda c = new Comanda(idComanda, mesa, nombre, fecha, total, totalMaximo, padre.getEmpleado());
                    try {
                        ComandaDB.crearComanda(c, lineasComanda);
                        JOptionPane.showMessageDialog(this, "Se ha creado la comanda correctamente.");
                        this.dispose();
                    } catch (MiExcepcion ex) {
                        JOptionPane.showMessageDialog(this, MiError.getMensaje(ex.getCodigo()));
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "La comanda no puede estar vacía.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "El total de la comanda no puede superar el total máximo.");
            }

        }
    }//GEN-LAST:event_btnAceptarActionPerformed


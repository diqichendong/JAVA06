    public DialogNuevaComanda(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();

        this.padre = (FramePrincipal) parent;
        this.total = 0;
        tablaLineasComanda.getColumnModel().getColumn(0).setPreferredWidth(255);
        tablaLineasComanda.getColumnModel().getColumn(1).setPreferredWidth(80);

        // Obtener datos por defecto para la nueva comanda
        try {
            idComanda = ComandaDB.getNuevaComandaId();
            lblId.setText(idComanda + "");
            lineasComanda = PlatoDB.getLineasComandaNueva();
        } catch (MiExcepcion ex) {
            JOptionPane.showMessageDialog(this, MiError.getMensaje(ex.getCodigo()));
            this.dispose();
        }

        // Rellenar tabla por defecto
        DefaultTableModel modelo = (DefaultTableModel) tablaLineasComanda.getModel();
        modelo.setRowCount(0);
        for (Plato p : lineasComanda.keySet()) {
            modelo.addRow(new Object[]{p, 0});
        }

        // Listener tabla
        modelo.addTableModelListener((e) -> {
            // Actualiza lineas de comanda
            for (int i = 0; i < modelo.getRowCount(); i++) {
                Plato p = (Plato) modelo.getValueAt(i, 0);
                Integer c = (Integer) modelo.getValueAt(i, 1);
                if (c == null) {
                    c = 0;
                }
                lineasComanda.put(p, c);
            }
            // Actualiza total
            total = 0;
            for (Plato p : lineasComanda.keySet()) {
                total += lineasComanda.get(p) * p.getPrecio();
            }
            lblTotal.setText(Utils.precioStr(total));
        });

        // Fecha por defecto
        datepicker.getComponentDateTextField().setEditable(false);
        datepicker.setDate(LocalDate.now());
    }


    private void btnFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotoActionPerformed
        try {
            JFileChooser fc = new JFileChooser();
            fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
            fc.addChoosableFileFilter(new FileNameExtensionFilter("Image Files", "jpg", "png"));
            fc.setAcceptAllFileFilterUsed(false);
            if (fc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
                File f = fc.getSelectedFile();
                this.foto = f.getName();
                this.pathFoto = f.toPath();
                ImageIcon imageIcon = new ImageIcon(f.getAbsolutePath());
                imageIcon = new ImageIcon(imageIcon.getImage()
                        .getScaledInstance(100, 100, Image.SCALE_SMOOTH));
                lblFoto.setIcon(imageIcon);
            }
        } catch (Exception ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "No se ha podido cargar la foto.");
        }
    }//GEN-LAST:event_btnFotoActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try {
            Validador v = new Validador();
            String nif = txtNif.getText().trim();

            if (v.checkNif(nif) == Validador.NIF_OK) {

                String nuevafoto = this.empleado.getId() + "." + this.foto.split("\\.")[1];
                Path copia = Paths.get("./src/imagenes/" + nuevafoto);
                Files.copy(this.pathFoto, copia, StandardCopyOption.REPLACE_EXISTING);
                
                this.empleado.setFoto(nuevafoto);
                this.empleado.setNif(nif);
                this.empleado.setUsuario(txtUsuario.getText().trim());
                this.empleado.setNombre(txtNombre.getText().trim());

                this.db.modificarEmpleado(empleado);

                JOptionPane.showMessageDialog(this, "Se ha modificado el empleado correctamente.");

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "El NIF no es v√°lido.");
            }

        } catch (MiExcepcion ex) {
            JOptionPane.showMessageDialog(this, MiError.getMensaje(ex.getCodigo()));
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "No se a podido modificar el empleado.");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

